#include <iostream>
using namespace std;
// string 字符存取
void test601()
{
	string str = "hello";

	// cout << "str = " << str << endl;

	// 1. 通过 [] 访问单个字符
	for (int i = 0; i < str.size(); i++)
	{
		cout << str[i] << " ";
	}
	cout << endl;

	// 2. 通过 at 方式访问字符
	for (int i = 0; i < str.size(); i++)
	{
		cout << str.at(i) << " ";
	}
	cout << endl;

	// 修改单个字符
	str[0] = 'x';
	// xello
	cout << "str = " << str << endl;

	str.at(1) = 'x';
	// xxllo
	cout << "str = " << str << endl;
}

int main6()
{
	test601();
	system("pause");
	return 0;
}